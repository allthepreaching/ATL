  <h3>HOW TO GO TO HEAVEN</h3>
  <ol>
    <li>
      <span class="main-li">Admit you are a sinner. </span>
      <ul class="styled">
        <li>
          “For all have sinned, and come short of the glory of God”
          (Romans 3:23)
        </li>
        <li>No one is good enough to go to Heaven on his own merit.</li>
      </ul>
    </li>
    <li>
      <span class="main-li">Realize the penalty for sin.</span>
      <ul class="styled">
        <li>“For the wages of sin is death” (Romans 6:23a)</li>
        <li>
          “And death and hell were cast into the lake of fire. This is the
          second death.” (Revelation 20:14)
        </li>
        <li>The penalty for breaking God’s law is death.</li>
      </ul>
    </li>
    <li>
      <span class="main-li">Believe that Jesus Christ died, was buried, and rose again for
        you</span>
      <ul class="styled">
        <li>
          “Christ died for our sins according to the scriptures; And that
          he was buried, and that he rose again the third day according to
          the scriptures” (1 Corinthians 15:3b-4)
        </li>
        <li>
          “For God so loved the world, that he gave his only begotten Son,
          that whosoever believeth in him should not perish, but have
          everlasting life.” (John 3:16)
        </li>
        <li>
          God loves you so much that he paid the penalty of your sins for
          you. To be saved we must believe (trust) that Jesus did this,
          and thereby realize that we do not earn salvation by obeying the
          Law but are simply saved by faith alone.
        </li>
        <li>
          “Knowing that a man is not justified by the works of the law,
          but by the faith of Jesus Christ, even we have believed in Jesus
          Christ, that we might be justified by the faith of Christ, and
          not by the works of the law: for by the works of the law shall
          no flesh be justified.” (Galatians 2:16)
        </li>
      </ul>
    </li>
    <li>
      <span class="main-li">Believe that your salvation is everlasting</span>
      <ul class="styled">
        <li>
          “He that believeth on the Son hath everlasting life: and he that
          believeth not the Son shall not see life; but the wrath of God
          abideth on him.” (John 3:36)
        </li>
        <li>
          Once we believe we have everlasting life. We will never
          experience the spiritual death of hell!
        </li>
        <li>
          “And whosoever liveth and believeth in me shall never die.
          Believest thou this?” (John 11:26)
        </li>
      </ul>
    </li>
    <li>
      <span class="main-li">Ask God for the gift of eternal life</span>
      <ul class="styled">
        <li>
          “For whosoever shall call upon the name of the Lord shall be
          saved.” (Romans 10:13)
        </li>
        <li>
          Eternal life is a gift purchased by the blood of Jesus and
          offered freely to those who call upon him by faith.
        </li>
      </ul>
    </li>
    <li>
      <span class="main-li">Let us help you word a prayer.</span>
      <ul class="styled">
        <li>
          Realize that it’s not mere words that save, but your faith in
          Jesus Christ.
        </li>
        <li>
          Dear Jesus, I know that I am a sinner, and I know that I deserve
          to go to hell for my sins. But, please save me and give me the
          gift of everlasting life. Thank You. Amen
        </li>
      </ul>
    </li>
  </ol>
